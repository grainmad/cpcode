void sol() {
    string s;
    cin >> s;
    int n = s.size();
    int a = 0, b = 0;
    for (char i:s) {
        if ('A' <= i && i <= 'Z') a++;
        else b++;
    }
    if (a > b) {
        for (auto& i:s) {
            if ('a' <= i && i <= 'z') i = char(i-'a'+'A');
        }
    } else {
        for (auto& i:s) {
            if ('A' <= i && i <= 'Z') i = char(i-'A'+'a');
        }
    }
    cout << s << "\n";
}

