template<class t,class u> ostream& operator<<(ostream& os,const map<t,u>& mp){
    os<<'{'; int s = 1;
    for(auto [x,y]:mp) { if (s) s = 0; else os << ", "; os<<x<<": "<<y; }
    return os<<'}';
}

ll phi(ll n) {
    ll rt = n;
    for (ll i=2; i*i<=n; i++) {
        if (n%i) continue;
        while (n%i == 0) n/=i;
        rt /= i;
        rt *= i-1;
    }
    if (n != 1) {
        rt /= n;
        rt *= n-1;
    }
    return rt;
}

void sol() {
    ll a, m;
    cin >> a >> m;
    cout << phi(m/gcd(a, m)) << endl;
}

