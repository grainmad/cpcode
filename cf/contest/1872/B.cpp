void sol() {
    int n;
    cin >> n;
    int ans = 1000;
    for (int i = 0; i < n; i++) {
        int d, s;
        cin >> d >> s;
        int t = s / 2;
        if (s % 2 == 0)
            t--;
        ans = min(ans, d + t);
    }
    cout << ans << "\n";
}

