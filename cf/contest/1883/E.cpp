using namespace std;

void sol() {
    int n;
    cin >> n;
    vector<ll> a(n);
    for (auto& i : a)
        cin >> i;
    int ans = 0;
    for (int i = 1; i < n; i++) {
        while (a[i] < a[i - 1]) {
            a[i] *= 2;
            ans++;
        }
    }
    cout << ans << "\n";
}

