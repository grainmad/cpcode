void sol() {
    int l1, r1, l2, r2;
    cin >> l1 >> r1 >> l2 >> r2;
    int mxl = max(l1, l2);
    int mnl = min(l1, l2);
    int mxr = max(r1, r2);
    int mnr = min(r1, r2);
    if (mxl > mnr) {
        cout << "1\n";
    } else {
        int ans = mnr-mxl;
        if (mnl<mxl) ans++;
        if (mxr>mnr) ans++;
        cout << ans << "\n";
    }
}

