void sol() {
    string s;
    cin >> s;
    int n = s.size();
    if (n == 1) {
        s.push_back(char((s[0]-'a'+1)%26+'a'));
        cout << s << "\n";
        return ;
    }
    int p = 1;
    while (p<n && s[p-1] != s[p]) p++;
    if (p == n) p = 1;
    char c = 'a';
    while (s[p-1] == c || c == s[p]) c = (c-'a'+1)%26+'a';
    cout << s.substr(0, p) << c << s.substr(p) << "\n";
}

